def build_user_prompt(page_text: str, prev_tail: str) -> str:
    parts = []
    if prev_tail:
        parts.append("【上一页结尾（仅供判断延续关系，不要重复输出）】\n" + prev_tail)
    parts.append("【本页原始文本】\n" + page_text)
    return "\n\n".join(parts)
